/*
ВЫВОД / ИНСАЙТ:
  1. Доминирование кредитных карт (более 73% всех оплат) говорит о том, 
   что клиенты Olist активно используют рассрочки (в Бразилии покупка в 
   рассрочку по кредитке — национальный стандарт потребления).
