Этот запрос считает общее количество успешных и активных заказов, а именно <<98 207>> на 
маркетплейсе, полностью исключая отмененные и недоступные сделки

ВЫВОД / ИНСАЙТ:
Мы получили базовую очищенную метрику объема продаж. Данное число 
